#include <stdio.h>

int main(){

    int a = 20;
    int b = 30;

    printf("a > b: %d\n", a > b);
    printf("a < b: %d\n", a < b);
    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n", a != b);

    return 0;

}

/*
Principais operadores relacionais

Os operadores relacionais disponíveis na linguagem C são:

> (maior que)
< (menor que)
>= (maior ou igual a)
<= (menor ou igual a)
== (igual a)
!= (diferente de)*/
